package com.entities;

import java.io.Serializable;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;

/**
 *
 * @author Jennifer
 */
@Entity
@Table(name = "users")
public class User implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    private Long id;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    @Column(name = "USERNAME")
    private String userName;//Nombre de usuario
    @Column(name = "USERPASSWORD")
    private String userPassword;//Password de usuario
    @Column(name="USERREALNAME")
    private String userRealName;//Nombre real del usuario
    @Column(name="USERSURNAME")
    private String userSurname;//Apellido del usuario
    @Column(name="USERID")
    private String userID;//DNI del usuario

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getUserPassword() {
        return userPassword;
    }

    public void setUserPassword(String userPassword) {
        this.userPassword = userPassword;
    }

    public String getUserRealName() {
        return userRealName;
    }

    public void setUserRealName(String userRealName) {
        this.userRealName = userRealName;
    }

    public String getUserSurname() {
        return userSurname;
    }

    public void setUserSurname(String userSurname) {
        this.userSurname = userSurname;
    }

    public String getUserID() {
        return userID;
    }

    public void setUserID(String userID) {
        this.userID = userID;
    }

    public User(){
        
    }
    
    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof User)) {
            return false;
        }
        User other = (User) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
       String newLine = System.lineSeparator();
        
        StringBuilder sb = new StringBuilder();
        sb.append("ID: "+this.id).append(newLine);
        sb.append("UserID: "+ this.userID).append(newLine);
        sb.append("Name: "+this.userRealName).append(newLine);
        sb.append("Surname: "+this.userSurname).append(newLine);
        sb.append("UserName: "+this.userName);
        
        return sb.toString();
    }

}
